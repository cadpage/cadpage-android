package net.anei.cadpage.parsers.MN;

import net.anei.cadpage.parsers.BaseParserTest;

import org.junit.Test;


public class MNBloomingtonParserTest extends BaseParserTest {
  
  public MNBloomingtonParserTest() {
    setParser(new MNBloomingtonParser(), "BLOOMINGTON", "MN");
  }
  
  @Test
  public void testParser() {

    doTest("T1",
        "RESIDENTIAL FIRE ALM AD: 9824 DELLRIDGE RD CMT1: AUDIBLE RESIDENTIAL FIRE ALARM NO ANSWER ON CALLING INSIDE 952-884-001",
        "CALL:RESIDENTIAL FIRE ALM",
        "ADDR:9824 DELLRIDGE RD",
        "INFO:AUDIBLE RESIDENTIAL FIRE ALARM NO ANSWER ON CALLING INSIDE 952-884-001");

    doTest("T2",
        "STRUCTURE FIRE AD: 8336 17 AV S CMT1: READING ON CO DETECTOR GAS ADN 225 - BEEPING - NO ONE FEELS ILL",
        "CALL:STRUCTURE FIRE",
        "ADDR:8336 17 AV S",
        "MADDR:8336 17 AVE S",
        "INFO:READING ON CO DETECTOR GAS ADN 225 - BEEPING - NO ONE FEELS ILL");

    doTest("T3",
        "SELECT STATION CALL APT: 6 AD: 25 98 ST W LOC: METRO APARTMENTS CMT1: STOVE WAS LEFT ON - NEED VENTILATION - OFFICER IS ON SCENE",
        "CALL:SELECT STATION CALL",
        "APT:6",
        "ADDR:25 98 ST W",
        "PLACE:METRO APARTMENTS",
        "INFO:STOVE WAS LEFT ON - NEED VENTILATION - OFFICER IS ON SCENE");

    doTest("T4",
        "RESET ALARM AD: 5230 102 ST W LOC: TARNHILL APARTMENTS CMT1: ON 3RD FLOOR WESTEND - PULL STATION ACTIVATED - FALSE ALARM - NEEDS",
        "CALL:RESET ALARM",
        "ADDR:5230 102 ST W",
        "PLACE:TARNHILL APARTMENTS",
        "INFO:ON 3RD FLOOR WESTEND - PULL STATION ACTIVATED - FALSE ALARM - NEEDS");

    doTest("T5",
        "STRUCTURE FIRE AD: 8100 HIGHWOOD DR LOC: FRIENDSHIP VILLAGE/M CMT1: MALE WITH LEG TRAPPED IN MECHANISM OF ELEVATOR",
        "CALL:STRUCTURE FIRE",
        "ADDR:8100 HIGHWOOD DR",
        "PLACE:FRIENDSHIP VILLAGE/M",
        "INFO:MALE WITH LEG TRAPPED IN MECHANISM OF ELEVATOR");

    doTest("T6",
        "ELEVATOR RESCUE AD: 5401 GREEN VALLEY DR LOC: CROWNE PLAZA HOTEL CMT1: ONE STUCK IN ELEVATOR FOR 20 MIN - IN SOUTH TOWER BET 5TH",
        "CALL:ELEVATOR RESCUE",
        "ADDR:5401 GREEN VALLEY DR",
        "PLACE:CROWNE PLAZA HOTEL",
        "INFO:ONE STUCK IN ELEVATOR FOR 20 MIN - IN SOUTH TOWER BET 5TH");
        
  }
  
  public static void main(String[] args) {
    new MNBloomingtonParserTest().generateTests("T1");
  }
}
